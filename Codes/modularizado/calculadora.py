def fatorial(n):
    if n == 0:
        return 1
    else:
        return n * fatorial(n - 1)
    
def soma(a, b):
    soma = a + b
    return soma

def subtracao(a, b):
    subtracao = a - b
    return subtracao

def multiplicacao(a, b):
    multiplicacao = a * b
    return multiplicacao

def divisao(a, b):
    divisao = a / b
    return divisao

def potencia(a, b):
    potencia = a ** b
    return potencia

